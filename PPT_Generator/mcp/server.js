#!/usr/bin/env node
/**
 * ============================================================================
 * KAARTECH PPT GENERATOR — MCP SERVER
 * ============================================================================
 * This MCP server exposes the PPT Generator to Claude Desktop so you can
 * say: "Generate a PPT for the 7 SAP Portals" and Claude will call this
 * server to produce the .pptx file.
 *
 * Tools exposed:
 *   1. generate_ppt          — Gemini AI generates content + builds .pptx
 *   2. generate_ppt_from_json — Build .pptx from pre-existing JSON content
 *   3. list_output_files      — List all generated .pptx files
 *   4. get_content_schema     — Return the JSON schema Gemini must follow
 *   5. preview_slide_count    — Count how many slides would be generated
 *   6. inspect_pptx           — Inspect slide structure of any .pptx
 *   7. edit_pptx              — Apply edit operations to a .pptx
 *
 * Setup:
 *   cd PPT_Generator/mcp
 *   npm install
 *   node server.js
 *
 * Claude Desktop config (claude_desktop_config.json):
 * {
 *   "mcpServers": {
 *     "kaartech-ppt": {
 *       "command": "node",
 *       "args": ["<path-to>/PPT_Generator/mcp/server.js"],
 *       "env": {
 *         "GEMINI_API_KEY": "YOUR_KEY_HERE"
 *       }
 *     }
 *   }
 * }
 * ============================================================================
 */

const { McpServer } = require("@modelcontextprotocol/sdk/server/mcp.js");
const { StdioServerTransport } = require("@modelcontextprotocol/sdk/server/stdio.js");
const { z } = require("zod");
const { spawn } = require("child_process");
const path = require("path");
const fs = require("fs");

// ── Paths (Windows-style, forward slashes also work) ──────────────────────
const BASE_DIR = path.resolve(__dirname, "..");
const SRC_DIR = path.join(BASE_DIR, "src");
const OUTPUT_DIR = path.join(BASE_DIR, "output");
const GENERATOR = path.join(SRC_DIR, "ppt_generator.py");
const EDITOR = path.join(SRC_DIR, "ppt_editor.py");
const ENV_FILE = path.join(BASE_DIR, ".env");

// Ensure output directory exists
if (!fs.existsSync(OUTPUT_DIR)) fs.mkdirSync(OUTPUT_DIR, { recursive: true });

// ── Read Gemini API key from env or .env file ──────────────────────────────
function getGeminiKey() {
  if (process.env.GEMINI_API_KEY) return process.env.GEMINI_API_KEY;
  if (fs.existsSync(ENV_FILE)) {
    const lines = fs.readFileSync(ENV_FILE, "utf-8").split("\n");
    for (const line of lines) {
      const m = line.match(/^GEMINI_API_KEY\s*=\s*(.+)$/);
      if (m) return m[1].trim().replace(/^["']|["']$/g, "");
    }
  }
  return null;
}

// ── Python runner ─────────────────────────────────────────────────────────
function runPython(scriptPath, args, env = {}) {
  return new Promise((resolve, reject) => {
    const merged = {
      ...process.env,
      GEMINI_API_KEY: getGeminiKey() || "",
      ...env,
    };
    // Windows: use "python", Linux/Mac: use "python3"
    const py = process.platform === "win32" ? "python" : "python3";
    const child = spawn(py, [scriptPath, ...args], {
      env: merged,
      cwd: SRC_DIR,
    });

    let stdout = "";
    let stderr = "";

    child.stdout.on("data", (d) => (stdout += d.toString()));
    child.stderr.on("data", (d) => (stderr += d.toString()));

    child.on("close", (code) => {
      if (code === 0) {
        resolve({ stdout, stderr, code });
      } else {
        reject(new Error(`Python exited ${code}:\n${stderr}\n${stdout}`));
      }
    });

    child.on("error", reject);
  });
}

